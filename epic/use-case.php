		<h1>Use Cases:</h1>
		<h2>Walter Lawrence - Pet Owner</h2>
		<h3>Use Case</h3>
		<p>Walter lost his dog after the leash slipped out of his hands when his poodle lunged after a pigeon.</p>
		<h3>User Story:</h3>
		<p>Walter needs a website where he can connect with people who have located missing pets.</p>
		<h3>Frustrations:</h3>
		<ul>
			<li>Not able to post pictures of lost pet</li>
			<li>Needs to provide contact information without providing info to spammers.</li>
		</ul>
		<h3>Interaction Flow:</h3>
		<ol>
			<li>After searching his neighborhood for hours to find his lost dog, Walt navigates to www.lostpaws.com on his smartphone, at the recommendation of a coworker.</li>
			<li>Walt is greeted to the LostPaws homepage with three options, Lost A Pet, Found A Pet, and I am already a registered user.</li>
			<li>Walt taps the Lost A Pet button.</li>
			<li>Walt is then presented with a heat map of lost pets in Albuquerque.</li>
			<li>Walt zooms in on his area of town and filters the map to only view lost dogs.</li>
			<li>Walt thinks to himself, “wow there are so many lost pets.”	Walt refines his search again to only see dogs found today.</li>
			<li>Unfortunately Walt did not find his dog. So he clicks the post lost pet button below the heat map.</li>
			<li>Walt sets up a user account by signing in with his google id, then provides LostPaws with the following information about his lost dog: Name, Chip#, Breed, Gender, Color, Size, and other distinguishing features.</li>
			<li>After submitting the lost dog posting, Walt is given the option to print out an automatically generated lost dog poster. Walt prints 80 copies and plasters every telephone pole in his neighborhood.</li>
			<li>Three weeks later Walt receives a phone call from a young college student named Margo. Margo found walt's dog in the Bosque.</li>
			<li>Walt updates his lost pet listing to "reunited."</li>
		</ol>



		<h2>Kristi Wilkins - Pet Finder</h2>
		<h3>Use Case&#58;</h3>
		<p>Kristi found a dog while on her commute home from work. She is looking for a way to let other people know so that she
			can return the dog to its rightful owner.</p>
		<h3>User Story&#58;</h3>
		<p>Kristi needs a website that allows her to make a post with relevant information on the dog, so she can
		get in contact with the owner and return the dog.</p>
		<h3>Frustrations&#58;</h3>
		<ul>
			<li>Not being able to post a picture of the dog.</li>
			<li>Unable to mark the region where the dog was found.</li>
			<li>Unable to easily correspond with the owner.</li>
		</ul>
		<h3>Interaction Flow&#58;</h3>
		<ul>
			<li>Kristi arrives at the homepage.</li>
			<li>The homepage has a button that says "Found A Lost Animal? Click Here!"</li>
			<li>A small popup appears asking her if she would like to make an account through Facebook, Google, or e-mail.</li>
			<li>Kristi decides to log in through Facebook for easy communication.</li>
			<li>Kristi is then brought to a new webpage.</li>
			<li>The webpage displays a space to upload a picture as well as multiple lines to fill out name, breed, etc.</li>
			<li>She fills out the information and scrolls down to a map with the ability to pin the area where the dog was found.</li>
			<li>She finalizes the post and waits to see if the owner gets in contact with her.</li>
			<li>A few days later Kristi receives a message via Facebook from Walter Lawrence.</li>
			<li>They schedule a time and place to meet so the dog can be returned to Walter.</li>
			<li>Walter is reunited with his dog and Kristi changes the status of her post to returned!</li>
		</ul>

