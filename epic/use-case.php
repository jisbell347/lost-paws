		<h2>Walter Lawrence - Pet Owner</h2>
		<h3>Use Case</h3>
		<p>Walter lost his dog after the leash slipped out of his hands when his heeler mix lunged after a pigeon.</p>
		<h3>User Story:</h3>
		<p>Walter needs a website where he can connect with people who have located missing pets.</p>
		<h3>Interaction Flow:</h3>
		<ol>
			<li>After searching his neighborhood for hours to find his lost dog, Walt navigates to www.lostpaws.com on his smart phone, at the recommendation of a coworker.</li>
			<li>Walt is greeted to the LostPaws homepage with three options, "Search Lost and Found Pets", "Report a Missing Pet", and "User Login".</li>
			<li>Walt taps the Search Lost and Found Pets button.</li>
			<li>Walt is then presented with a list of reported pets in Albuquerque.</li>
			<li>Walt clicks the filter menu and filters results to only view found dogs reported today.</li>
			<li>Unfortunately Walt did not find his dog. So he clicks the "Report a Missing Pet" button in the navigation at the bottom of the page.</li>
			<li>Walt sets up a user account by signing in with his Google id, clicks the "I have lost my pet" radio button, then provides LostPaws with the following information about his lost dog: species(cat or dog), name, description, distinguishing features, date lost, and location last seen.</li>
			<li>Walt uploads the best photo from his iPhone's photo library that shows the best overall view of Chico.</li>
			<li>Walt reviews his posting and clicks submit.</li>
			<li>One week later Walt receives a phone call from a young college student named Margo. Margo found walt's dog in the Bosque. Walter arranges to meet with Margo the same day at the local police station to be reunited with his beloved heeler mix.</li>
			<li>Walt updates his lost pet listing to "found."</li>
		</ol>



		<h2>Kristi Wilkins - Pet Finder</h2>
		<h3>Use Case&#58;</h3>
		<p>Kristi found a dog while on her commute home from work. She is looking for a way to let other people know so that she
			can return the dog to its rightful owner.</p>
		<h3>User Story&#58;</h3>
		<p>Kristi needs a website that allows her to make a post with relevant information on the dog, so she can
		get in contact with the owner and return the dog.</p>
		<h3>Interaction Flow&#58;</h3>
		<ul>
			<li>Kristi arrives at the homepage.</li>
			<li>The homepage displays buttons showing Search Lost and Found Pets, Report a Missing Pet, and User Login.</li>
			<li>Kristi clicks on search and is shown a page with a list of lost dogs.</li>
			<li>She clicks filter and selects by most recent.</li>
			<li>Kristi looks through the search results but doesn't find a post for the dog she found.</li>
			<li>Kristi decides to login so that she can make a post about the dog and clicks on User login.</li>
			<li>She is met with a small popup asking for login information or create an account.</li>
			<li>She clicks login through Facebook.</li>
			<li>Kristi is brought to the homepage and clicks Report a Missing Pet.</li>
			<li>The webpage displays spaces to fill out the info she has for the dog, such as name, description, etc.</li>
			<li>She then uploads one photo of the dog she found.</li>
			<li>She finalizes the post and waits to see if the owner gets in contact with her.</li>
			<li>A few days later Kristi receives a message via email from the owner.</li>
			<li>They schedule a time and place to meet so the dog can be returned to the owner.</li>
			<li>The owner is reunited with his dog and Kristi changes the status of her post to returned!</li>
		</ul>
